/**
 * Таблица транслитерации кириллицы в латиницу
 */
const TRANSLIT_MAP = {
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd',
    'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
    'й': 'j', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
    'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
    'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch',
    'ш': 'sh', 'щ': 'shch', 'ъ': '', 'ы': 'y', 'ь': '',
    'э': 'e', 'ю': 'yu', 'я': 'ya',
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D',
    'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I',
    'Й': 'J', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N',
    'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T',
    'У': 'U', 'Ф': 'F', 'Х': 'Kh', 'Ц': 'Ts', 'Ч': 'Ch',
    'Ш': 'Sh', 'Щ': 'Shch', 'Ъ': '', 'Ы': 'Y', 'Ь': '',
    'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
};

/**
 * Транслитерация кириллических символов в латиницу.
 * @param {string} text
 * @returns {string}
 */
export function transliterate(text) {
    return text
        .split('')
        .map(char => TRANSLIT_MAP[char] !== undefined ? TRANSLIT_MAP[char] : char)
        .join('');
}

/**
 * Генерация slug из произвольного текста.
 * Транслитерирует кириллицу, приводит к нижнему регистру,
 * заменяет спецсимволы и пробелы на дефисы.
 * @param {string} text
 * @returns {string}
 */
export function generateSlug(text) {
    return transliterate(text)
        .toLowerCase()
        .replace(/[^a-z0-9\s-]/g, '')  // убираем всё кроме латиницы, цифр, пробелов, дефисов
        .replace(/\s+/g, '-')           // пробелы → дефисы
        .replace(/-+/g, '-')            // убираем множественные дефисы
        .replace(/^-|-$/g, '');         // убираем дефисы в начале и конце
}
